namespace MGS\OSCheckout\Model\System\Config\Source;

class AddressSuggest
{
    /**
     * @return array
     */
    public function getTriggerOption()
    {
        return [
            '' => __('No'),
            'google' => __('Google'),
        ];
    }

    /**
     * @return array
     */
    public function toOptionArray()
    {
        $options = [];
        foreach ($this->getTriggerOption() as $code => $label) {
            $options[] = [
                'value' => $code,
                'label' => $label
            ];
        }

        return $options;
    }
}
